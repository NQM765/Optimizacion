"""## 2. Produccion semanal de una fabrica"""

# PRODUCCION SEMANAL DE UNA FABRICA

import numpy as np

# --- DATOS DEL PROBLEMA ---
# S_n = numero de sillas producidas en la semana n
# M_n = numero de mesas producidas en la semana n
#
# S_(n+1) = 0.6*S_n + 0.2*M_n + 40
# M_(n+1) = 0.1*S_n + 0.5*M_n + 20

S0 = 200
M0 = 80

# --- MATRIZ DEL SISTEMA ---
A = np.array([
    [0.6, 0.2],
    [0.1, 0.5],
])
b = np.array([40, 20])
x0 = np.array([S0, M0])

# --- CALCULAR SEMANA 1 Y 2 ---
x1 = A @ x0 + b
x2 = A @ x1 + b

# --- EQUILIBRIO ---
# En equilibrio se cumple: x = A*x + b
# (I - A)x = b
equilibrio = np.linalg.solve(np.eye(2) - A, b)

