# EPIDEMIA EN UNA CIUDAD

import numpy as np

# --- DATOS DEL PROBLEMA ---
# I(t) = numero de infectados
# S(t) = numero de susceptibles
# Poblacion total: S(t) + I(t) = 10000
#
# dI/dt = beta*(10000 - I)*I - gamma*I

N = 10000
beta = 0.00003
gamma = 0.1
I0 = 50
S0 = N - I0

# --- FUNCION DEL MODELO ---
def dI_dt(I):
    return beta * (N - I) * I - gamma * I

# --- PUNTOS DE EQUILIBRIO ---
# 0 = I * (beta*(N - I) - gamma)
I_eq_1 = 0.0
I_eq_2 = N - (gamma / beta)

S_eq_1 = N - I_eq_1
S_eq_2 = N - I_eq_2

